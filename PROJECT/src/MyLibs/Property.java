package MyLibs;

/**
 *
 * @author Carlo Miguel Legaspi
 */
public class Property {
    
    private int block;
    private int lot;
    private Double cost;
    private Double sqkm;

    public int getBlock() {
        return block;
    }

    public void setBlock(int block) {
        this.block = block;
    }

    public int getLot() {
        return lot;
    }

    public void setLot(int lot) {
        this.lot = lot;
    }

    public Double getCost() {
        return cost;
    }

    public void setCost(Double cost) {
        this.cost = cost;
    }

    public Double getSqkm() {
        return sqkm;
    }

    public void setSqkm(Double sqkm) {
        this.sqkm = sqkm;
    }
    
}
